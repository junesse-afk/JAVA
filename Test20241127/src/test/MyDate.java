package test;

public class MyDate {
	private String name;
	private int year;
	private int month;
	private int day;
	
	public void setName(String name) {
		this.name=name;
	}
	
	public void setYear(int year) {
		this.year=year;
	}
	
	public void setMonth(int month) {
		this.month=month;
	}
	
	public void setDay(int day) {
		this.day=day;
	}
	
	public String getName() {
		return name;
	}
	public int getYear() {
		return year;
	}
	public int getMonth() {
		return month;
	}
	public int getDay() {
		return day;
	}

	 
}
